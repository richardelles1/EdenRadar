const CYCLE_S = 6;

interface WorldMapProps {
  width?: number;
  height?: number;
  isDark?: boolean;
  className?: string;
}

export function WorldMap({
  width = 480,
  height = 240,
  isDark = true,
  className = "",
}: WorldMapProps) {
  const clipId = useId().replace(/:/g, "");
  const containerRef = useRef<HTMLDivElement>(null);
  const inView = useInView(containerRef, { once: false, amount: 0.3 });

  const landDotColor = isDark ? "rgba(255,255,255,0.18)" : "rgba(0,0,0,0.15)";
  const dotColor = isDark ? "hsl(142 65% 60%)" : "hsl(142 52% 36%)";
  const arcColor = isDark ? "hsl(142 65% 55%)" : "hsl(142 52% 36%)";
  const arcOpacity = isDark ? 0.60 : 0.50;

  const landCoords = useMemo(
    () => LAND_DOTS.map(([lat, lng]) => project(lat, lng)),
    []
  );

  const dotCoords = useMemo(
    () => TTO_DOTS.map(([lat, lng]) => project(lat, lng)),
    []
  );

  const arcData = useMemo(
    () =>
      ARC_PAIRS.map(([[lat1, lng1], [lat2, lng2]], i) => ({
        d: arcPath(lat1, lng1, lat2, lng2),
        delay: (i / ARC_PAIRS.length) * CYCLE_S * 0.9,
      })),
    []
  );

  return (
    <div
      ref={containerRef}
      className={className}
      style={{ width, height, position: "relative", overflow: "hidden" }}
    >
      <svg
        viewBox={`0 0 ${W} ${H}`}
        width={width}
        height={height}
        style={{ display: "block" }}
        aria-hidden
      >
        <defs>
          <clipPath id={`wm-clip-${clipId}`}>
            <rect x={0} y={0} width={W} height={H} />
          </clipPath>
        </defs>

        <g clipPath={`url(#wm-clip-${clipId})`}>
          {/* Dotted landmass */}
          {landCoords.map(([x, y], i) => (
            <circle
              key={`land-${i}`}
              cx={x}
              cy={y}
              r={2.2}
              fill={landDotColor}
            />
          ))}

          {/* TTO location dots */}
          {dotCoords.map(([x, y], i) => (
            <circle
              key={`tto-${i}`}
              cx={x}
              cy={y}
              r={2.8}
              fill={dotColor}
              opacity={0.75}
            />
          ))}

          {/* Animated arcs — staggered, back and forth */}
          {inView &&
            arcData.map(({ d, delay }, i) => (
              <g key={i}>
                <motion.path
                  d={d}
                  fill="none"
                  stroke={arcColor}
                  strokeWidth={1.5}
                  strokeLinecap="round"
                  initial={{ pathLength: 0, opacity: 0 }}
                  animate={{
                    pathLength: [0, 1, 1, 0],
                    opacity: [0, arcOpacity, arcOpacity, 0],
                  }}
                  transition={{
                    pathLength: {
                      duration: CYCLE_S,
                      delay,
                      repeat: Infinity,
                      repeatDelay: 1.2,
                      ease: "easeInOut",
                    },
                    opacity: {
                      duration: CYCLE_S,
                      delay,
                      repeat: Infinity,
                      repeatDelay: 1.2,
                      ease: "easeInOut",
                      times: [0, 0.18, 0.82, 1],
                    },
                  }}
                />
                <motion.circle
                  r={3.5}
                  fill={arcColor}
                  initial={{ opacity: 0 }}
                >
                  <animateMotion
                    path={d}
                    dur={`${CYCLE_S}s`}
                    begin={`${delay}s`}
                    repeatCount="indefinite"
                    calcMode="spline"
                    keySplines="0.4 0 0.6 1"
                  />
                  <animate
                    attributeName="opacity"
                    values={`0;${arcOpacity};${arcOpacity};0`}
                    keyTimes="0;0.18;0.82;1"
                    dur={`${CYCLE_S}s`}
                    begin={`${delay}s`}
                    repeatCount="indefinite"
                  />
                </motion.circle>
              </g>
            ))}
        </g>
      </svg>
    </div>
  );
}
